// observable class

#include <cmath> // for std::sqrt

class observable {
public:
  observable() : sum_(0.), esq_(0.), count_(0) {}
  void operator<<(double x) { sum_ += x; esq_ += x * x; ++count_; }
  double mean() const { return (count_ > 0) ? (sum_ / count_) : 0.; }
  double error() const {
    return (count_ > 1) ?
      std::sqrt((esq_ / count_ - mean() * mean()) / (count_ - 1)) : 0.;
  }
private:
  double sum_, esq_;
  unsigned int count_;
};
